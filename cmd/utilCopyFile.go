import (
	"io"
	"log"
	"os"
)

////////////////////////////////////////////////////////////////////////////////////////////////////

// copy file
func copyFile(orig, dest string) {
	// handle origin
	origFile, ε := os.Open(orig)
	if ε != nil {
		log.Fatal(ε)
	}
	defer origFile.Close()

	// handle destiny
	destFile, ε := os.Create(dest)
	if ε != nil {
		log.Fatal(ε)
	}
	defer destFile.Close()

	// copy file
	_, ε = io.Copy(destFile, origFile)
	if ε == nil {
		origInfo, ε := os.Stat(orig)
		if ε != nil {
			ε = os.Chmod(dest, origInfo.Mode())
		}
	}
}

