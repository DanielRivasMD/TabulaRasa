// copy & replace
type paramsCopyReplace struct {
	orig  string
	dest  string
	files []string
	reps  []rep
}

////////////////////////////////////////////////////////////////////////////////////////////////////

func createCopyReplace() paramsCopyReplace {
	π := paramsCopyReplace{}
	π.files = []string{}
	π.reps = []rep{}
	return π
}

func replaceCopyReplace(target string, reps []rep) paramsCopyReplace {
	π := paramsCopyReplace{}
	π.orig = target
	π.reps = reps
	return π
}

func copyCopyReplace(orig, dest string) paramsCopyReplace {
	π := createCopyReplace()
	π.orig = orig
	π.dest = dest
	return π
}

func cloneCopyReplace(π paramsCopyReplace) paramsCopyReplace {
	Ω := createCopyReplace()
	Ω.orig = π.orig
	Ω.dest = π.dest
	Ω.files = π.files
	Ω.reps = π.reps
	return Ω
}

