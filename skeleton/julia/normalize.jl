# load packages
begin
  using Statistics
end

####################################################################################################

"Normalize data by Z-score"
function zScore(ɒ)
  (ɒ .- mean(ɒ)) ./ std(ɒ)
end

####################################################################################################

"Normalize data by min-max"
function minMax(ɒ)
  (ɒ .- minimum(ɒ)) ./ (maximum(ɒ) - minimum(ɒ))
end

